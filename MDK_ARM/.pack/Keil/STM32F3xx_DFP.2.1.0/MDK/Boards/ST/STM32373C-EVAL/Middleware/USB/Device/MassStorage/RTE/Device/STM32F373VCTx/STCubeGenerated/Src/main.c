/**
  * Override default HAL_GetTick function
  */
uint32_t HAL_GetTick (void) {
  static uint32_t ticks = 0U;
         uint32_t i;

  if (osKernelGetState () == osKernelRunning) {
    return ((uint32_t)osKernelGetTickCount ());
  }

  /* If Kernel is not running wait approximately 1 ms then increment
     and return auxiliary tick counter value */
  for (i = (SystemCoreClock >> 14U); i > 0U; i--) {
    __NOP(); __NOP(); __NOP(); __NOP(); __NOP(); __NOP();
    __NOP(); __NOP(); __NOP(); __NOP(); __NOP(); __NOP();
  }
  return ++ticks;
}

/**
  Callback function used to read Card Detect (CD) pin state when
  Memory Card is used in SPI mode.
  \param[in]  drive_num                Memory Card Drive number
  \return     1:card detected, 0:card not detected, or error
*/
int32_t fs_mc_read_cd (uint32_t drive_num) {

  if (drive_num == 0U) {
    if (HAL_GPIO_ReadPin(MX_PE3_GPIOx, MX_PE3_GPIO_Pin) == GPIO_PIN_RESET) {
      return 1U;
    }
  }

  return 0U;
}

/**
  Callback function used to control Slave Select signal when
  Memory Card is used in SPI mode.
  \param[in]  drive_num                Memory Card Drive number
  \param[in]  ss                       Slave select signal state (0=inactive, 1=active)
  \return     slave select signal state
*/
int32_t fs_mc_spi_control_ss (uint32_t drive_num, uint32_t ss) {
  static bool spi_reconfigure = true;

  if (spi_reconfigure == true) {
    spi_reconfigure = false;

    Driver_SPI3.Control (ARM_SPI_MODE_MASTER | ARM_SPI_CPOL0_CPHA0  \
                                             | ARM_SPI_DATA_BITS(8) \
                                             | ARM_SPI_MSB_LSB      \
                                             | ARM_SPI_SS_MASTER_UNUSED,
                                               400000);
  }

  if (drive_num == 0U) {
    if (ss == 1U) {
      HAL_GPIO_WritePin(MX_PE2_GPIOx, MX_PE2_GPIO_Pin, GPIO_PIN_RESET);
    } else {
      HAL_GPIO_WritePin(MX_PE2_GPIOx, MX_PE2_GPIO_Pin, GPIO_PIN_SET);
    }

    return ((int32_t)ss);
  }

  return -1;
}

